#include "hash_tables.h"

/**
 * hash_table_print - Prints a hash table.
 * @ht: Pointes to the hash table.
 *
 * Description: Print Key/value pairs in the order
 *              they appear in the array of the hash table.
 */
void hash_table_print(const hash_table_t *ht)
{
	hash_node_t *node;
	unsigned long int i;
	unsigned char my_sign = 0;

	if (ht == NULL)
		return;

	printf("{");
	for (i = 0; i < ht->size; i++)
	{
		if (ht->array[i] != NULL)
		{
			if (my_sign == 1)
				printf(", ");

			node = ht->array[i];
			while (node != NULL)
			{
				printf("'%s': '%s'", node->key, node->value);
				node = node->next;
				if (node != NULL)
					printf(", ");
			}
			my_sign = 1;
		}
	}
	printf("}\n");
}
